function Game(data, container) {
  this.data = data;
  this.container = container;
  this.compareArr = [];
  this.win = 0;
  this.totalScore = 0;
}

Game.prototype.init = function() {
  const icons = this.data.map((item, index) => {
    let div;
    div = `<div class="inner inactive" data-number=${index}>${item}</div>`;
    return div;
  });

  const arr = this.randomSort(icons.concat(icons));
  document.querySelector('.score').innerHTML = this.totalScore;

  document.querySelector('.score').style.setProperty('--score-color', '#f00');
  this.render(arr);
  this.addListener();
};

Game.prototype.randomSort = function(arr) {
  const sortedArray = arr.sort(function() {
    return Math.random() - 0.5;
  });
  return sortedArray;
};

Game.prototype.render = function(sortedArray) {
  sortedArray.forEach(item => {
    const itemEl = document.createElement('div');
    itemEl.className = 'item';
    itemEl.innerHTML = item;
    container.appendChild(itemEl);
  });
};

Game.prototype.reloadGame = function() {
  swal({
    title: 'Play again?',
    type: 'question',
    animation: false,
    customClass: 'animated bounceInLeft',
    showCancelButton: true,
    confirmButtonColor: '#3085d6',
    cancelButtonColor: '#d33',
    confirmButtonText: 'Yes, play again!'
  }).then(result => {
    if (result.value) {
      swal({
        animation: false,
        customClass: 'animated bounceInLeft',
        title: 'New game started',
        type: 'success'
      });
      this.deleteAll();
    }
  });
};

Game.prototype.deleteAll = function() {
  while (this.container.firstChild) {
    this.container.removeChild(this.container.firstChild);
  }
  this.win = 0;
  setTimeout(() => {
    const game1 = new Game(icons);
    game1.init();
  }, 2000);
};

Game.prototype.addListener = function() {
  this.container.addEventListener('click', e => {
    if (e.target.classList.contains('item')) {
      e.target.firstChild.classList.remove('inactive');
      this.compare(e.target.firstChild);
    } else if (e.target.classList.contains('inner')) {
      e.target.remove('inactive');
      this.compare(e.target);
    } else if (e.target.tagName === 'svg') {
      e.target.parentElement.classList.remove('inactive');
      this.compare(e.target.parentElement);
    } else if (e.target.tagName === 'path') {
      e.target.parentElement.parentElement.classList.remove('inactive');
      this.compare(e.target.parentElement.parentElement);
    }
  });
};

Game.prototype.compare = function(el) {
  this.compareArr.push(el);
  if (this.compareArr.length < 2) return false;

  if (this.compareArr[0].dataset.number !== this.compareArr[1].dataset.number) {
    setTimeout(() => {
      this.compareArr[0].classList.add('inactive');
      this.compareArr[1].classList.add('inactive');
      this.compareArr = [];
    }, 200);
    this.totalScore -= 1;
  } else {
    this.compareArr = [];
    this.totalScore += 2;
    ++this.win;
  }
  if (this.win === 8) {
    this.reloadGame();
  }
  document.querySelector('.score').innerHTML = this.totalScore;
  if (this.totalScore >= 0) {
    document.querySelector('.score').style.setProperty('--score-color', '#0f0');
  } else {
    document.querySelector('.score').style.setProperty('--score-color', '#f00');
  }
};

